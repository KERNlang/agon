import { existsSync, mkdirSync, readdirSync, readFileSync, statSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { parseWithDiagnostics } from '@kernlang/core';

const GENERATED_HEADER_RE = /^\/\/ @generated by kern\b/;
const TRACEABLE_NODE_TYPES = new Set([
  'action',
  'class',
  'config',
  'const',
  'enum',
  'error',
  'event',
  'fn',
  'hook',
  'interface',
  'machine',
  'model',
  'repository',
  'screen',
  'service',
  'store',
  'type',
  'union',
]);

function escapeRegex(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function listKernFiles(rootDir) {
  const resolvedRoot = path.resolve(rootDir);
  if (!existsSync(resolvedRoot)) return [];

  const files = [];
  const stack = [resolvedRoot];

  while (stack.length > 0) {
    const current = stack.pop();
    let stat;
    try {
      stat = statSync(current);
    } catch {
      continue;
    }

    if (stat.isDirectory()) {
      for (const child of readdirSync(current)) {
        if (child === 'node_modules' || child.startsWith('.')) continue;
        stack.push(path.join(current, child));
      }
      continue;
    }

    if (stat.isFile() && current.endsWith('.kern')) files.push(current);
  }

  return files.sort();
}

function generatedCandidatesFor(sourceFile, srcDir, outDir) {
  const rel = path.relative(path.resolve(srcDir), sourceFile);
  const parsed = path.parse(rel);
  const relBase = path.join(parsed.dir, parsed.name);
  const base = path.resolve(outDir, relBase);

  return [
    { file: `${base}.ts`, entry: false },
    { file: `${base}.tsx`, entry: false },
    { file: `${base}.js`, entry: false },
    { file: `${base}.jsx`, entry: false },
    { file: `${base}.mjs`, entry: false },
    { file: `${base}.cjs`, entry: false },
    { file: `${base}.vue`, entry: false },
    { file: `${base}.py`, entry: false },
    { file: `${base}.entry.tsx`, entry: true },
  ];
}

function extractTracePoints(sourceFile) {
  const source = readFileSync(sourceFile, 'utf8');
  const parsed = parseWithDiagnostics(source);
  const sourceName = path.basename(sourceFile, '.kern');
  const root = parsed.root;
  const nodes = [];
  const stack = root ? [root] : [];
  while (stack.length > 0) {
    const node = stack.pop();
    if (!node) continue;
    nodes.push(node);
    for (const child of [...(node.children ?? [])].reverse()) stack.push(child);
  }

  return nodes
    .filter((node) => node?.loc?.line && TRACEABLE_NODE_TYPES.has(node.type))
    .map((node) => {
      const name = typeof node.props?.name === 'string' ? node.props.name : null;
      return {
        sourceName,
        line: node.loc.line,
        nodeType: node.type,
        name,
      };
    })
    .filter((point) => point.name);
}

function declarationPatterns(point) {
  const name = escapeRegex(point.name);
  return [
    new RegExp(`^(\\s*)(?:export\\s+)?(?:default\\s+)?(?:async\\s+)?function\\s+${name}\\b`),
    new RegExp(`^(\\s*)(?:export\\s+)?const\\s+${name}\\b`),
    new RegExp(`^(\\s*)(?:export\\s+)?let\\s+${name}\\b`),
    new RegExp(`^(\\s*)(?:export\\s+)?class\\s+${name}\\b`),
    new RegExp(`^(\\s*)(?:export\\s+)?interface\\s+${name}\\b`),
    new RegExp(`^(\\s*)(?:export\\s+)?type\\s+${name}\\b`),
    new RegExp(`^(\\s*)(?:export\\s+)?enum\\s+${name}\\b`),
  ];
}

function headerInsertIndex(lines) {
  if (lines.length === 0) return 0;

  let index = 0;
  if (lines[index]?.startsWith('#!')) index += 1;
  if (GENERATED_HEADER_RE.test(lines[index] ?? '')) {
    index += 1;
    if (lines[index] === '') index += 1;
  }
  return index;
}

function insertTraceComments(content, tracePoints) {
  if (tracePoints.length === 0) return content;

  const lines = content.split('\n');
  const unmatched = [];

  for (const point of tracePoints) {
    const comment = `// @kern-source: ${point.sourceName}:${point.line}`;
    let inserted = false;

    for (let index = 0; index < lines.length; index += 1) {
      if (lines[index - 1] === comment) {
        inserted = true;
        break;
      }

      const match = declarationPatterns(point).some((pattern) => pattern.test(lines[index]));
      if (!match) continue;

      lines.splice(index, 0, comment);
      inserted = true;
      break;
    }

    if (!inserted) unmatched.push(comment);
  }

  if (unmatched.length > 0) {
    lines.splice(headerInsertIndex(lines), 0, ...unmatched, '');
  }

  return lines.join('\n');
}

function entryTracePoints(tracePoints) {
  const screenPoint = tracePoints.findLast((point) => point.nodeType === 'screen');
  const fallback = tracePoints[0];
  return [screenPoint ?? fallback].filter(Boolean);
}

export function addKernSourceTraces(srcDir, outDir) {
  const resolvedSrc = path.resolve(srcDir);
  const resolvedOut = path.resolve(outDir);
  if (!existsSync(resolvedSrc) || !existsSync(resolvedOut)) return { files: 0, updated: 0 };

  let files = 0;
  let updated = 0;

  for (const sourceFile of listKernFiles(resolvedSrc)) {
    const tracePoints = extractTracePoints(sourceFile);
    if (tracePoints.length === 0) continue;

    for (const candidate of generatedCandidatesFor(sourceFile, resolvedSrc, resolvedOut)) {
      if (!existsSync(candidate.file)) continue;
      files += 1;

      const candidateTracePoints = candidate.entry ? entryTracePoints(tracePoints) : tracePoints;
      const before = readFileSync(candidate.file, 'utf8');
      const after = insertTraceComments(before, candidateTracePoints);
      if (after === before) continue;

      mkdirSync(path.dirname(candidate.file), { recursive: true });
      writeFileSync(candidate.file, after);
      updated += 1;
    }
  }

  return { files, updated };
}

